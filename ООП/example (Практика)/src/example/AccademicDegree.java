package example;

public class AccademicDegree { //Ученая степень
	private String degreeName; //имя степени

	public AccademicDegree(String degreeName) {
		super();
		this.degreeName = degreeName;
	}

	public String getDegreeName() {
		return degreeName;
	}
	
	public void setDegreeName(String degreeName) {
		this.degreeName = degreeName;
	}
	
}
